namespace DomainModel;

public class Instructor
{
    
        int id;
        string firstName;
        string lastName;
        string email;

        public int getId() 
        {
            return this.id;
        }
        public string getFirstName() 
        {
            return this.firstName;
        }
        public string getLastName() 
        {
            return this.lastName;
        }
        public string getEmail() 
        {
            return this.email;
        }

    
}